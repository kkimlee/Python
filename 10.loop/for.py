for number in [1,2,3,4,5]: # for number in range(0,5)
  print("hello")
  print(number)

for alphabet in 'abcdefg':
    print(alphabet)

for str in ['Ameicano', 'latte', 'frafuchino']:
    print(str)

for increase in range(1, 10, 2): # 1부터 9까지 2씩 증가시키면서 반복문 수행
    print(increase)

for decrease in range(10, 1, -1): # 10부터 2까지 -1씩 감소시키면서 박복문 수행
    print(decrease)
