# * 연산자
print(3 * 3 * 3 * 3 * 3) # 3을 다섯번 곱함
print(3 ** 5) # 3의 5승

# % 연산자
print(7 / 2) # 7을 2로 나눈 몫
print(7 // 2)
print(7 % 2) # 7을 2로 나눈 나머지

# 증가 또는 감소 연산
int_a = 1 # 1 대입
int_a = int_a + 1 # a에 1더한후 a에 대입
print(int_a)

int_a += 1 # a 증가 연산
print(int_a)

int_a = int_a - 1 # a에 1뺀후 a에 대입
int_a -= 1 # a에 감소 연산
print(int_a)
